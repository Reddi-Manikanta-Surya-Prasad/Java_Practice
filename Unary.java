public class Unary {
    public static void main(String []args) {
        int a = 10;
        int b = 20;
        a++;
        System.out.println("a increment: "+a);
        a--;
        System.out.println("a decrement: "+a);
        b--;
        System.out.println("b decrement: "+b);
        b++;
        System.out.println("b increment: "+b);
    }
}
